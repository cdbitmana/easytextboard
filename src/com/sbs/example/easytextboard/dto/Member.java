package com.sbs.example.easytextboard.dto;

public class Member {
	private int number;
	private String id;
	private String pw;
	private String name;

	public int getNumber() {
		return number;
	}

	public String getId() {
		return id;
	}

	public String getPw() {
		return pw;
	}

	public String getName() {
		return name;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public void setId(String id) {
		this.id = id;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}

	public void setName(String name) {
		this.name = name;
	}

}
